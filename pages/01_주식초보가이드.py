import streamlit as st

# ==========================================
# 1. 페이지 기본 설정
# ==========================================
st.set_page_config(page_title="주식 초보 가이드", page_icon="🐣", layout="wide")

st.title("🐣 주린이를 위한 주식 초보 가이드")
st.markdown("""
당곡고등학교 학생들과 주식을 처음 접하는 분들을 위한 안내서입니다. 
주식이 무엇인지 이해하고, 현명한 경제 관념을 기르기 위한 기초 지식을 쌓아보세요!
""")

st.divider() # 구분선

# ==========================================
# 2. 주식이란 무엇인가요?
# ==========================================
st.header("1. 주식(Stock)이란 무엇인가요?")

col1, col2 = st.columns([1, 2])
with col1:
    st.image("https://img.freepik.com/free-vector/stock-market-concept-illustration_114360-6435.jpg", use_column_width=True)
    st.caption("기업은 성장을 위해 사람들의 투자를 받습니다.")
    
with col2:
    st.info("""
    **주식은 '회사의 소유권'을 잘게 쪼갠 조각입니다.**
    
    여러분이 삼성전자나 애플의 주식을 1주 샀다면, 아주 작지만 그 회사의 '공동 주인(주주)'이 된 것입니다.
    회사가 장사를 잘해서 돈을 많이 벌면 주식의 가치(주가)가 올라가서 이익을 얻을 수 있고, 
    번 돈의 일부를 '배당금'이라는 형태로 용돈처럼 나눠주기도 합니다.
    """)
    st.warning("**주의할 점:** 반대로 회사가 어려워지면 주식의 가치가 떨어져서 투자한 돈을 잃을 수도 있습니다. 그래서 투자는 신중해야 해요!")

st.write("") # 빈 줄로 간격 띄우기

# ==========================================
# 3. 주식은 어떻게 시작하나요? (Step-by-Step)
# ==========================================
st.header("2. 주식 투자는 어떻게 시작하나요?")
st.write("주식 시장에서 직접 투자를 해보려면 아래의 4단계를 거치게 됩니다.")

# columns를 활용해 4단계를 가로로 나란히 배치
step1, step2, step3, step4 = st.columns(4)

with step1:
    st.success("STEP 1. 계좌 개설 🏦")
    st.write("은행에 가서 통장을 만들듯, '증권사' 앱을 통해 주식 거래용 통장을 만듭니다. (미성년자는 부모님의 동의가 필요해요!)")

with step2:
    st.success("STEP 2. 투자금 입금 💰")
    st.write("주식 계좌에 내가 투자할 만큼의 돈(여윳돈)을 이체합니다.")

with step3:
    st.success("STEP 3. 기업 분석 🔍")
    st.write("아무 회사나 사면 안 되겠죠? 우리가 만든 이 웹앱을 통해 수익률과 차트를 분석하며 좋은 회사를 고릅니다.")

with step4:
    st.success("STEP 4. 매수와 매도 🤝")
    st.write("원하는 가격에 주식을 사는 것을 **'매수'**, 나중에 가격이 올랐을 때 파는 것을 **'매도'**라고 합니다.")

st.write("")

# ==========================================
# 4. 필수 주식 용어 사전 (Expander 활용)
# ==========================================
st.header("3. 차트 분석 필수 용어 사전")
st.write("우리 웹앱의 다른 페이지(수익률 비교, 집중 분석)에서 쓰인 용어들을 알아볼까요?")

# st.expander를 사용하여 클릭하면 내용이 펼쳐지도록 구성
with st.expander("❓ 티커(Ticker)란 무엇인가요?"):
    st.write("""
    전 세계의 수많은 주식회사를 쉽게 찾기 위해 부여한 **알파벳이나 숫자로 된 기호**입니다.
    - 한국: 종목코드 6자리 숫자 (예: 삼성전자 = `005930`)
    - 미국: 알파벳 약자 (예: 애플 = `AAPL`, 테슬라 = `TSLA`)
    """)

with st.expander("❓ 캔들 차트(봉 차트)는 어떻게 읽나요?"):
    st.write("""
    하루 동안 주가가 어떻게 움직였는지 양초(Candle) 모양으로 그린 그래프입니다.
    - **시가(Open):** 아침에 주식 시장이 열렸을 때의 첫 가격
    - **종가(Close):** 오후에 주식 시장이 닫힐 때의 마지막 가격
    - **고가(High) / 저가(Low):** 하루 중 가장 비쌌던 가격 / 가장 쌌던 가격
    - 한국은 주가가 오르면 **빨간색(양봉)**, 내리면 **파란색(음봉)**으로 표시합니다.
    """)

with st.expander("❓ 이동평균선(Moving Average)이란?"):
    st.write("""
    일정 기간(예: 5일, 20일) 동안의 평균 주가를 점으로 찍어 선으로 이은 것입니다.
    매일매일 널뛰기하는 주가 대신, **전체적인 가격의 흐름(추세)**이 올라가고 있는지 내려가고 있는지 파악할 때 유용하게 쓰입니다.
    """)

st.divider()

# ==========================================
# 5. 마무리 인사
# ==========================================
st.markdown("### 👨‍💻 이제 준비가 끝났습니다!")
st.write("왼쪽 사이드바의 **'한/미 주요 주식 수익률 비교 분석'**이나 **'삼성전자 집중 분석'** 메뉴로 이동해서 실제 주식 데이터를 직접 분석해 보세요!")
